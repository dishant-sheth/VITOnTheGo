public class Place {

    public String place_name;
    public String place_location;
    public String place_details;
    public String place_rating;


    public Place(){

    }





    public String getPlace_rating() {
        return place_rating;
    }

    public void setPlace_rating(String place_rating) {
        this.place_rating = place_rating;
    }

    public String getPlace_name() {
        return place_name;
    }

    public void setPlace_name(String place_name) {
        this.place_name = place_name;
    }

    public String getPlace_location() {
        return place_location;
    }

    public void setPlace_location(String place_location) {
        this.place_location = place_location;
    }

    public String getPlace_details() {
        return place_details;
    }

    public void setPlace_details(String place_details) {
        this.place_details = place_details;
    }





}
